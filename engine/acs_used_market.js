/* ============================================================
   💰 MONEY FORMATTER SAFE
   ============================================================ */

function ACS_usedFormatUSD(value) {
  const n = Number(value || 0);
  if (!Number.isFinite(n)) return "$0";
  return "$" + Math.round(n).toLocaleString("en-US");
}

/* ============================================================
   🧼 HTML ESCAPE
   ============================================================ */

function ACS_escapeHTML(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

/* ============================================================
   🖼️ ACS USED MARKET IMAGE RESOLVER — BUY NEW SYNC v1.0
   ------------------------------------------------------------
   Purpose:
   - Use the same aircraft image logic as Buy New
   - Backend Used Market rows may not include "model"
   - Normalize from aircraft_name / model_key
   - JPG first because ACS aircraft library uses mostly JPG
   - Fallback to PNG, then placeholder
   ============================================================ */

function ACS_slugAircraftName(value) {
  return String(value || "")
    .toLowerCase()
    .trim()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

function ACS_cleanUsedImageModelName(ac) {
  if (!ac) return "";

  const manufacturer = String(ac.manufacturer || "").trim();

  let model = String(
    ac.model ||
    ac.aircraft_model ||
    ac.aircraft_name ||
    ac.model_key ||
    ""
  ).trim();

  if (manufacturer) {
    const re = new RegExp(
      "^" + manufacturer.replace(/[.*+?^${}()|[\]\\]/g, "\\$&") + "\\s+",
      "i"
    );
    model = model.replace(re, "").trim();
  }

  model = model
    .replace(/^boeing\s+/i, "")
    .replace(/^douglas\s+/i, "")
    .replace(/^lockheed\s+/i, "")
    .replace(/^airbus\s+/i, "")
    .replace(/^avro\s+/i, "")
    .replace(/^convair\s+/i, "")
    .replace(/^de\s+havilland\s+/i, "")
    .replace(/^vickers\s+/i, "")
    .replace(/^beechcraft\s+/i, "")
    .replace(/^fokker\s+/i, "")
    .replace(/^ford\s+/i, "")
    .replace(/^junkers\s+/i, "")
    .trim();

  return model;
}

