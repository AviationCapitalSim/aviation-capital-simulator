/* ============================================================
   5) RENDER DE TARJETAS — BACKEND DATA
   ============================================================ */

function renderUsedMarket(filter = "all") {
  const grid = document.getElementById("usedGrid");
  if (!grid) return;

  grid.innerHTML = "";

  const list = ACS_USED_MARKET_STATE.used_market || [];

  const filtered = filter === "all"
    ? list
    : list.filter(ac => ac.manufacturer === filter);

  if (ACS_USED_MARKET_STATE.loading) {
    grid.innerHTML = `
      <div style="grid-column:1/-1; padding:2rem; color:#ffb300;">
        Loading Used Aircraft Market...
      </div>
    `;
    return;
  }

  if (ACS_USED_MARKET_STATE.error) {
    grid.innerHTML = `
      <div style="grid-column:1/-1; padding:2rem; color:#ff6b6b;">
        ❌ Used Aircraft Market unavailable.<br>
        <small>${ACS_escapeHTML(ACS_USED_MARKET_STATE.error)}</small>
      </div>
    `;
    return;
  }

  if (!filtered.length) {
    grid.innerHTML = `
      <div style="grid-column:1/-1; padding:2rem; color:#dbe4ff;">
        No aircraft available for this filter.
      </div>
    `;
    return;
  }

  filtered.forEach(ac => {
    const card = document.createElement("div");
    card.className = "used-card";

    const title = ac.aircraft_name ||
      `${ac.manufacturer || ""} ${ac.model_key || ""}`.trim();

    const yearBuilt = ac.year_built ?? "N/A";
    const hours = Number(ac.total_hours || 0);
    const cycles = Number(ac.total_cycles || 0);
    const condition = Number(ac.condition_pct || 0);
    const marketPrice = Number(ac.market_price || 0);
    const source = ac.market_source || "UNKNOWN";
    const broker = ac.remarketing_agent || "N/A";

    card.innerHTML = `
      <img src="${getAircraftImage(ac)}"
           onerror="ACS_handleImageFallback(this)"
           alt="${ACS_escapeHTML(title)}" />

      <h3>${ACS_escapeHTML(title)}</h3>

      <p>Manufacturer: ${ACS_escapeHTML(ac.manufacturer || "N/A")}</p>
      <p>Year Built: ${ACS_escapeHTML(yearBuilt)}</p>
      <p>Age: ${ACS_escapeHTML(ac.age_years ?? "N/A")} years</p>
      <p>Hours: ${hours.toLocaleString()}</p>
      <p>Cycles: ${cycles.toLocaleString()}</p>
      <p>Condition: ${condition.toFixed(0)}%</p>
      <p>Source: ${ACS_escapeHTML(source)}</p>
      <p>Broker: ${ACS_escapeHTML(broker)}</p>
      <p><b>Price: ${ACS_usedFormatUSD(marketPrice)}</b></p>

      <button class="ac-buy" onclick="buyUsed('${ac.id}')">
        BUY
      </button>
      <button class="ac-lease" onclick="leaseUsed('${ac.id}')">
        LEASE
      </button>
      <button class="ac-info" onclick="openInfo('${ac.id}')">
        INFO
      </button>
    `;

    grid.appendChild(card);
  });
}

